#Expressions
alpha = 20;
beta = 40;
print(f'Addition: {alpha + beta}');#20+40
print(f'Subtration: {alpha - beta}');#20-40
print(f'Multiplication: {alpha * beta}');#20*40
print(f'Division: {alpha / beta}');#20/40
print(f'Power: {alpha ** beta}'); #20^40
print(f'Remainder: {alpha % beta}');#20%40 remainder when divided

#Order Of Calculating
randomValue = 50;
answer = 5 * (2 + randomValue) - 23 / 4 % 10;
print(answer);